def linguistique (language):
    if language == "Javascript":
        return "Tu es développeur Web"
    elif language == "Python":
        return "Tu es développeur IA"
    elif language == "Java":
        return "Tu es développeur logiciel"
    elif language == "reactjs":
        return "Tu es développeur mobile"
    else:
        return "Un jour je serai le meilleur développeur... Je coderai sans répis !"
    
print(linguistique("Javascript"))
print(linguistique("Java"))
print(linguistique("reactjs"))
print(linguistique("Calculatrice"))
print(linguistique("Python"))